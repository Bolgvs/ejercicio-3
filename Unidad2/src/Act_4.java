public class Act_4 {
	
	public static void main (String args []) {
        
        // Variables y vista de resultados:

        int N = 910;
        
        System.out.println("El valor inicial de N es: " + N);
        
        N += 77;
        
        System.out.println("N + 77: " + N);
        
        N -= 3;
        
        System.out.println("N - 3: " + N);
        
        N *= 2;
        
        System.out.println("N * 2: " + N);
        
	}
}
