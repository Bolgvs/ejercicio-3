
public class Act4_2 {
	
	public static void main (String args []) {
        
        // Valores:

        int N = 19;
        
        double A = 89.6;
        
		char C = 'a';

        // Impresión de Variables:
        
        System.out.println("Variable N = " + N + "\nVariable A = " + A + "\nVariable C = " + C);
        
        // Operaciones numéricas:
        
        double suma = N + A;

        double resta = A - N;
        
        // Impresiones de resultados
        
        System.out.println(N + " + " + A + " = " + suma);
        
        System.out.println(A + " - " + N + " = " + resta);
        
        System.out.println("Valor numérico del carácter a = " + (int) C);
        
        
	}
}
